	@Test
	void testSteadyStateTorque() {
		// Fitzgerald, Electrical machinery, 2003
		double v1 = 230 / ExtendedMath.SQRT3;
		int nph = 3;
		int poles = 4;
		double fe = 60;
		double r1 = .095, x1 = .68, x2 = .672, xm = 18.7;

		// page 328
		double[] slips = new double[200];
		for (int i = 0; i < 200; i++) {
			slips[i] = (200. - i) / 200.;
		}
		double[][] tm = InductionMotor.tm(nph, v1, r1, .1, x1, x2, xm, fe, fe, slips, poles);
		for (int i = 0; i < 200; i++) {
			tm[0][i] *= 60;
		}

		Assertions.assertArrayEquals(R2_EQUALS_DOT1[0], tm[0], 1e-9);
		Assertions.assertArrayEquals(R2_EQUALS_DOT1[1], tm[1], 1e-9);

		// page 330
		tm = InductionMotor.tm(nph, v1, r1, .3, x1, x2, xm, fe, fe, new double[] { 1. - 1719. / 1800. }, poles);
		Assertions.assertEquals(36.8, tm[1][0], .1);
	}

